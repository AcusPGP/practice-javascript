// Tenary operator = toán tử ba ngôi

// Declared variables
var isAnimal = 1; // Non zero integer -> True; Zero integer -> False

// Conditional operator
if (isAnimal) {
     console.log("Yes, it is an animal!");
} else {
     console.log("No, it is not an animal!");
}

// Tenary operator = Simplier conditional operator
var result = isAnimal ? "Yes, it is an animal!" : "No, it is not an animal!";
console.log(result);