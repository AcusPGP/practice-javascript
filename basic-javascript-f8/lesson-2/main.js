// Declare variables
var a = 5, b = 2;
     
// Addition
console.log(a + b); // 7

// Substraction
console.log(a - b); // 3

// Multiplication
console.log(a * b); // 10

// Exponentiation
console.log(a**b) // 25

// Division
console.log(a / b) // 2

// Modulus 
console.log(a % b) // 1

// Increment
console.log(a++) // 6
console.log(++a) // 5

// Decrement
console.log(b--) // 1
console.log(--b) // 0